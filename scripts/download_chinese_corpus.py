"""
下载轻量级中文语料库用于预训练
使用 wiki_cn_filtered 数据集（中文维基百科精简版）
"""
import os
import json
from datasets import load_dataset

def main():
    output_dir = "/home/sd/Megatron-DeepSpeed/data"
    output_file = os.path.join(output_dir, "chinese_wiki.jsonl")
    
    print("=" * 60)
    print("下载中文维基百科语料库")
    print("=" * 60)
    
    # 使用 pleisto/wikipedia-cn-20230720-filtered 数据集
    # 这是一个经过清洗的中文维基百科数据集，约500MB
    print("\n正在下载数据集...")
    
    try:
        # 尝试使用中文维基百科数据集
        dataset = load_dataset("pleisto/wikipedia-cn-20230720-filtered", split="train")
        text_field = "completion"
    except Exception as e:
        print(f"下载 pleisto/wikipedia-cn-20230720-filtered 失败: {e}")
        print("尝试使用备选数据集...")
        try:
            # 备选：使用 wikitext 中文版
            dataset = load_dataset("wikipedia", "20220301.zh", split="train[:50000]")
            text_field = "text"
        except Exception as e2:
            print(f"备选数据集也失败: {e2}")
            print("使用本地生成的扩展数据集...")
            generate_extended_dataset(output_file)
            return
    
    print(f"数据集大小: {len(dataset)} 条")
    
    # 转换为JSONL格式
    print(f"\n正在转换为JSONL格式: {output_file}")
    
    count = 0
    with open(output_file, 'w', encoding='utf-8') as f:
        for item in dataset:
            text = item.get(text_field, "")
            if text and len(text.strip()) > 100:  # 过滤太短的文本
                json.dump({"text": text.strip()}, f, ensure_ascii=False)
                f.write('\n')
                count += 1
                if count % 10000 == 0:
                    print(f"  已处理 {count} 条...")
    
    print(f"\n完成! 共保存 {count} 条文本到 {output_file}")
    
    # 统计文件大小
    file_size = os.path.getsize(output_file) / (1024 * 1024)
    print(f"文件大小: {file_size:.2f} MB")

